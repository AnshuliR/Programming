package Array;

public class Example1 {
    public static void main(String[] args) {

        //byte type array
        byte arr[] = {2,4,5};
        System.out.println(arr[0]);
        System.out.println(arr[1]);
        System.out.println(arr[2]);
        System.out.println();

        //short type array
        short a[] = {2,3,4};
        System.out.println(a[0]);
        System.out.println(a[1]);
        System.out.println(a[2]);
        System.out.println();

        //int type array
        int a1[] = {2,4,5};
        System.out.println(a1[0]);
        System.out.println(a1[1]);
        System.out.println(a1[2]);
        System.out.println();

        //long type array
        long a2[] = {666,478,5122};
        System.out.println(a2[0]);
        System.out.println(a2[1]);
        System.out.println(a2[2]);
        System.out.println();

        //float type array
        float a3[] = {2.3f,4.2f,5.3f};
        System.out.println(a3[0]);
        System.out.println(a3[1]);
        System.out.println(a3[2]);
        System.out.println();

        //double type array
        double a4[] = {2.67,4.22,5.33};
        System.out.println(a4[0]);
        System.out.println(a4[1]);
        System.out.println(a4[2]);
        System.out.println();

        //char type array
        char a5[] = {'a','b','c'};
        System.out.println(a5[0]);
        System.out.println(a5[1]);
        System.out.println(a5[2]);
        System.out.println();

        //String type array
        String s[] = {"My", "name","is","Anshuli"};
        System.out.println(s[0]);
        System.out.println(s[1]);
        System.out.println(s[2]);
        System.out.println(s[3]);
        
    }
}
